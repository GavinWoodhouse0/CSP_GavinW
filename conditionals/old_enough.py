age = int(input("How old are you?\n"))

if age >= 21:
    print("You're old enought to drink! This is a VERY important age. Be responsible.")
elif age >= 18:
    print("You are now a legal adult, which allows you to do so much that it would take ages to list them all.")
elif age >= 15:
    print("Depending on who and where you are, you're probably able to drive!")
elif age >= 13:
    print("You're probably in junior high now or at least will soon be, but more importantly you can enjoy at lot more movies and video games.")